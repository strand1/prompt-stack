/**
 * Helper to create icon button element
 */
export function createIconButton(type, title, className = '') {
  const btn = document.createElement('button');
  btn.type = 'button';
  btn.className = `card-action-btn ${className}`;
  btn.title = title;
  btn.innerHTML = Icons[type] ? Icons[type]() : '';
  btn.setAttribute('aria-label', title);
  return btn;
}